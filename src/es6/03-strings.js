let hello = "Hello";
let world = "World";
let boringWay = hello + " " + world;

// Template strings - Template literals
let fantasticWay = `${hello} ${world}!`;
console.log(fantasticWay);

// Multiple lines
let oldWayMultipleLines = "Hello\n" + "World!";
console.log(oldWayMultipleLines);

let multipleLines = `${hello}
${world}!`;
console.log(multipleLines);
